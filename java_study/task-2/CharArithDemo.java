class CharArithDemo {
	public static void main(String args[]) {
		char ch;

		ch = 'X';
		System.out.println("ch содержит " + ch);

		ch++; // инкрементировать переменную ch, 'Y'
		System.out.println("теперь ch содержит " + ch);

		ch = 90; // присвоить переменной ch значение 'Z'
		 System.out.println("теперь ch содержит " + ch);
	}
}